public class Conditionals {
    public static void main(String[] args) {

        int age = 20;

        if (age > 18) {
            System.out.println("Boy you can drive");

        } else {
            System.out.println("Boy you can't drive");
        }

        // lOGICAL OPERATORS (OR)

        boolean a = true;
        boolean b = false;

        System.out.println(!a);
        System.out.println(!b);
    }
}
